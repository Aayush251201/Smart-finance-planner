    private void regsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regsubmitActionPerformed
        if(regusr.getText().equals("") || regdob.getText().equals("") || regpass.getText().equals("") || regno.getText().equals("") || regmail.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please fill all the details");
        }
        else
        {
            String un,db,ps,cn,em;
            un=regusr.getText().toString();
            db=regdob.getText().toString();
            ps=regpass.getText().toString();
            cn=regno.getText().toString();
            em=regmail.getText().toString();
            createFolder();
            createFile(un);
            addData(un,ps,db,cn,em);
            JOptionPane.showMessageDialog(this, "Successfully registered!");
            dispose();
            GUILogin l=new GUILogin();
            l.setVisible(true);
        }
    }//GEN-LAST:event_regsubmitActionPerformed

