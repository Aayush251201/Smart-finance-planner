     void createFile(String usn)
    {
        try {
            FileWriter fw=new FileWriter(f+"\\"+usn+".txt");
        } catch (IOException ex) {
            Logger.getLogger(GUILogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     
     void addData(String usn, String psd, String dob, String con, String eml)
     {
       try {
           RandomAccessFile raf=new RandomAccessFile(f+"\\"+usn+".txt","rw");
           raf.writeBytes(usn+"\r\n");
           raf.writeBytes(psd+"\r\n");
           raf.writeBytes(dob+"\r\n");
           raf.writeBytes(con+"\r\n");
           raf.writeBytes(eml+"\r\n");
       } catch (FileNotFoundException ex) {
           Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
       } catch (IOException ex) {
           Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
       }
     }
    
